console.log(`Minimus is ${min}`);

// Max
let max = Math.max(12, 34, 65, 1, 13, 90, 334, 109, 145, 546, 0, 45);
console.log(`Minimus is ${max}`);

// Random
let random = Math.random();
console.log(random);

